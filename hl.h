static inline void hl_revcomp(uint64_t len, char *seq) // reverse complement in place
{
	uint64_t i;
	for (i = 0; i < len>>1; ++i) {
		uint8_t t = seq[len - i - 1];
		seq[len - i - 1] = hl_comp_table[(uint8_t)seq[i]];
		seq[i] = hl_comp_table[t];
	}
	if (len&1) seq[len>>1] = hl_comp_table[(uint8_t)seq[len>>1]];
}
